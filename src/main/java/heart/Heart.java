package heart;

public class Heart {
	private int bbsID;
	private String userID;
	private int likeBbs;

	public int getBbsID() {
		return bbsID;
	}
	public void setBbsID(int bbsID) {
		this.bbsID = bbsID;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public int getLikeBbs() {
		return likeBbs;
	}
	public void setLikeBbs(int likeBbs) {
		this.likeBbs = likeBbs;
	}	
}